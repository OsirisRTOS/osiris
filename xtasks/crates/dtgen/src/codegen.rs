fn emit_nodes(dt: &DeviceTree) -> TokenStream {
    let entries = dt.nodes.iter().map(|node| {
        let name = node.name.as_str();
        let phandle = node
            .phandle
            .map_or(quote! { None }, |v| quote! { Some(#v) });
        let parent = node.parent.map_or(quote! { None }, |v| quote! { Some(#v) });
        let children = &node.children;

        quote! {
            TreeNode {
                name: #name,
                phandle: #phandle,
                parent: #parent,
                children: &[#(#children),*],
            },
        }
    });

    quote! {
        pub const NODES: &[TreeNode] = &[
            #(#entries)*
        ];
    }
}

// ------------------------------------------------------------------------------------------------
// Peripherals
// ------------------------------------------------------------------------------------------------

fn emit_peripherals(dt: &DeviceTree) -> TokenStream {
    let mut indices: Vec<usize> = Vec::new();
    dt.walk(|idx, node| {
        if !node.compatible.is_empty() {
            indices.push(idx);
        }
    });

    let mut backing_statics = TokenStream::new();
    let mut peripheral_entries = Vec::new();

    for (i, &idx) in indices.iter().enumerate() {
        let node = &dt.nodes[idx];
        let mut sorted_extra: Vec<(&String, &PropValue)> = node.extra.iter().collect();
        sorted_extra.sort_by_key(|(k, _)| k.as_str());

        // backing statics for non-inline prop types
        for (j, (_, val)) in sorted_extra.iter().enumerate() {
            match val {
                PropValue::U32Array(arr) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}", i, j);
                    backing_statics.extend(quote! {
                        const #ident: &[u32] = &[#(#arr),*];
                    });
                }
                PropValue::StringList(list) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}_STRS", i, j);
                    backing_statics.extend(quote! {
                        const #ident: &[&str] = &[#(#list),*];
                    });
                }
                PropValue::Bytes(b) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}_BYTES", i, j);
                    backing_statics.extend(quote! {
                        const #ident: &[u8] = &[#(#b),*];
                    });
                }
                _ => {}
            }
        }

        // props array
        let props_ident = quote::format_ident!("PERIPH_{}_PROPS", i);
        let prop_entries = sorted_extra.iter().enumerate().map(|(j, (key, val))| {
            let val_tokens = match val {
                PropValue::Empty => quote! { PropValue::Empty },
                PropValue::U32(v) => quote! { PropValue::U32(#v) },
                PropValue::U32Array(_) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}", i, j);
                    quote! { PropValue::U32Array(#ident) }
                }
                PropValue::Str(s) => quote! { PropValue::Str(#s) },
                PropValue::StringList(_) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}_STRS", i, j);
                    quote! { PropValue::StringList(#ident) }
                }
                PropValue::Bytes(_) => {
                    let ident = quote::format_ident!("PERIPH_{}_PROP_{}_BYTES", i, j);
                    quote! { PropValue::Bytes(#ident) }
                }
            };

            quote! { (#key, #val_tokens), }
        });

        backing_statics.extend(quote! {
            const #props_ident: &[(&str, PropValue)] = &[
                #(#prop_entries)*
            ];
        });

        // peripheral entry
        let compat_strs = node.compatible.iter().map(|c| c.as_str());
        let reg_tokens = match node.reg {
            Some((base, size)) => {
                let base_lit =
                    syn::LitInt::new(&format!("{base:#010x}"), proc_macro2::Span::call_site());
                let size_lit =
                    syn::LitInt::new(&format!("{size:#x}"), proc_macro2::Span::call_site());

                quote! { Some((#base_lit, #size_lit)) }
            }
            None => quote! { None },
        };

        // automatic fallback to &[] on empty
        let irqs = &node.interrupts;
        let phandle_tokens = match node.phandle {
            Some(v) => quote! { Some(#v) },
            None => quote! { None },
        };

        peripheral_entries.push(quote! {
            Peripheral {
                node: #idx,
                compatible: &[#(#compat_strs),*],
                reg: #reg_tokens,
                interrupts: &[#(#irqs),*],
                phandle: #phandle_tokens,
                props: #props_ident,
            },
        });
    }

    quote! {
        #backing_statics

        pub const PERIPHERALS: &[Peripheral] = &[
            #(#peripheral_entries)*
        ];
    }
}
