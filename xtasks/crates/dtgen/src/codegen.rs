// Alias module
// ------------------------------------------------------------------------------------------------

fn emit_aliases_module(out: &mut String, dt: &DeviceTree) {
    let mut pairs: Vec<(String, usize)> = dt
        .by_name
        .get("aliases")
        .and_then(|indices| indices.first()) // there is usually only one /aliases node
        .map(|&idx| {
            dt.nodes[idx]
                .extra
                .iter()
                .filter_map(|(k, val)| {
                    if let crate::ir::PropValue::Str(path) = val {
                        // resolve the full path to a unique index
                        resolve_path(dt, path).map(|target_idx| (k.clone(), target_idx))
                    } else {
                        None
                    }
                })
                .collect()
        })
        .unwrap_or_default();

    // sort by alias name (e.g., serial0, serial1) for deterministic output
    pairs.sort_by(|a, b| a.0.cmp(&b.0));

    out.push('\n');

    out.push_str(&format!("// {}\n", "-".repeat(96)));
    out.push_str("// Aliases\n");
    out.push_str(&format!("// {}\n\n", "-".repeat(96)));

    out.push_str("pub mod aliases {\n");
    out.push_str("    use super::*;\n\n");

    // emit the slice of tuples: (alias_name, node_index)
    out.push_str("    pub const ALIASES: &[(&str, usize)] = &[\n");
    for (name, idx) in &pairs {
        out.push_str(&format!("        ({:?}, {}),\n", name, idx));
    }

    out.push_str("    ];\n\n");
    out.push_str(
        r#"    // resolve an alias name to its Peripheral
    pub fn resolve(alias: &str) -> Option<&'static Peripheral> {
        ALIASES
            .iter()
            .find(|(k, _)| *k == alias)
            .and_then(|(_, idx)| peripheral_by_node(*idx))
    }
"#,
    );

    out.push_str("}\n");
}

