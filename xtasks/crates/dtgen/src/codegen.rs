fn emit_memory_module(dt: &DeviceTree) -> TokenStream {
    let mut regions: Vec<(&str, u64, u64)> = dt
        .nodes
        .iter()
        .filter(|n| n.name.starts_with("memory@") || n.name == "memory")
        .filter_map(|n| {
            let (base, size) = n.reg?;
            Some((n.name.as_str(), base, size))
        })
        .collect();
    regions.sort_by_key(|&(_, base, _)| base);

    let entries = regions.iter().map(|(name, base, size)| {
        let base = *base as usize;
        let size = *size as usize;

        quote! { (#name, #base, #size), }
    });

    quote! {
        pub mod memory {
            use super::*;

            #[doc = "physical memory regions"]
            pub const REGIONS: &[(&str, usize, usize)] = &[
                #(#entries)*
            ];

            #[doc = "find a memory region by node name"]
            pub fn region_by_name(name: &str) -> Option<(usize, usize)> {
                REGIONS
                    .iter()
                    .find(|(n, _, _)| *n == name)
                    .map(|(_, base, size)| (*base, *size))
            }

            #[doc = "total memory in bytes across all declared regions"]
            pub fn total_bytes() -> usize {
                REGIONS.iter().map(|(_, _, size)| size).sum()
            }
        }
    }
}

fn emit_chosen_module(dt: &DeviceTree) -> TokenStream {
    let chosen = dt
        .by_name
        .get("chosen")
        .and_then(|indices| indices.first())
        .map(|&idx| &dt.nodes[idx]);

    let mut periph_indices: Vec<usize> = Vec::new();
    dt.walk(|idx, node| {
        if !node.compatible.is_empty() {
            periph_indices.push(idx);
        }
    });

    let stdout_periph_idx: Option<usize> = chosen.and_then(|n| {
        if let Some(crate::ir::PropValue::Str(raw)) = n.extra.get("stdout-path") {
            let path = raw.split(':').next().unwrap_or(raw);
            let node_idx = if path.contains('/') {
                resolve_path(dt, path)
            } else {
                dt.by_name
                    .get("aliases")
                    .and_then(|ids| ids.first())
                    .and_then(|&aidx| {
                        if let Some(crate::ir::PropValue::Str(resolved)) =
                            dt.nodes[aidx].extra.get(path)
                        {
                            resolve_path(dt, resolved)
                        } else {
                            None
                        }
                    })
            }?;
            periph_indices.iter().position(|&idx| idx == node_idx)
        } else {
            None
        }
    });

    let bootargs: Option<&str> = chosen.and_then(|n| {
        if let Some(crate::ir::PropValue::Str(s)) = n.extra.get("bootargs") {
            Some(s.as_str())
        } else {
            None
        }
    });

    const KNOWN: &[&str] = &["stdout-path", "bootargs"];
    let extras: Vec<(&str, &crate::ir::PropValue)> = chosen
        .map(|n| {
            let mut v: Vec<_> = n
                .extra
                .iter()
                .filter(|(k, _)| !KNOWN.contains(&k.as_str()))
                .map(|(k, v)| (k.as_str(), v))
                .collect();
            v.sort_by_key(|(k, _)| *k);
            v
        })
        .unwrap_or_default();

    // backing statics for array types that can't be expressed inline
    let backing_statics = extras
        .iter()
        .enumerate()
        .filter_map(|(i, (_, val))| match val {
            crate::ir::PropValue::U32Array(arr) => {
                let ident = quote::format_ident!("CHOSEN_EXTRA_{}", i);
                Some(quote! {
                    static #ident: &[u32] = &[#(#arr),*];
                })
            }
            crate::ir::PropValue::Bytes(arr) => {
                let ident = quote::format_ident!("CHOSEN_EXTRA_{}", i);
                Some(quote! {
                    static #ident: &[u8] = &[#(#arr),*];
                })
            }
            _ => None,
        });

    let stdout_tokens = match stdout_periph_idx {
        Some(idx) => quote! {
            #[doc = "index into PERIPHERALS of the stdout peripheral, resolved from stdout-path"]
            pub const STDOUT: Option<usize> = Some(#idx);
        },
        None => quote! {
            pub const STDOUT: Option<usize> = None;
        },
    };

    let bootargs_tokens = match bootargs {
        Some(s) => quote! {
            pub const BOOTARGS: Option<&str> = Some(#s);
        },
        None => quote! {
            pub const BOOTARGS: Option<&str> = None;
        },
    };

    let extra_entries = extras.iter().enumerate().map(|(i, (key, val))| {
        let val_tokens = match val {
            crate::ir::PropValue::Empty => quote! { PropValue::Empty },
            crate::ir::PropValue::U32(v) => {
                let lit = syn::LitInt::new(&format!("{v:#010x}"), proc_macro2::Span::call_site());
                quote! { PropValue::U32(#lit) }
            }
            crate::ir::PropValue::U32Array(_) => {
                let ident = quote::format_ident!("CHOSEN_EXTRA_{}", i);
                quote! { PropValue::U32Array(#ident.to_vec()) }
            }
            crate::ir::PropValue::Str(s) => quote! { PropValue::Str(#s) },
            crate::ir::PropValue::StringList(sl) => {
                quote! { PropValue::StringList(vec![#(#sl),*]) }
            }
            crate::ir::PropValue::Bytes(_) => {
                let ident = quote::format_ident!("CHOSEN_EXTRA_{}", i);
                quote! { PropValue::Bytes(#ident.to_vec()) }
            }
        };
        quote! { (#key, #val_tokens), }
    });

    quote! {
        #(#backing_statics)*

        pub mod chosen {
            use super::*;

            #stdout_tokens
            #bootargs_tokens

            #[doc = "non-standard properties of the chosen node"]
            pub const EXTRAS: &[(&str, PropValue)] = &[
                #(#extra_entries)*
            ];

            #[doc = "resolve stdout to its Peripheral directly via PERIPHERALS index"]
            pub fn stdout() -> Option<&'static Peripheral> {
                STDOUT.map(|idx| &PERIPHERALS[idx])
            }
        }
    }
}
