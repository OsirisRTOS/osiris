fn emit_aliases_module(dt: &DeviceTree) -> TokenStream {
    let pairs: Vec<(String, usize)> = dt
        .by_name
        .get("aliases")
        .and_then(|indices| indices.first())
        .map(|&idx| {
            dt.nodes[idx]
                .extra
                .iter()
                .filter_map(|(k, val)| {
                    if let crate::ir::PropValue::Str(path) = val {
                        resolve_path(dt, path).map(|target_idx| (k.clone(), target_idx))
                    } else {
                        None
                    }
                })
                .collect()
        })
        .unwrap_or_default();

    let mut pairs = pairs;
    pairs.sort_by(|a, b| a.0.cmp(&b.0));

    let entries = pairs.iter().map(|(name, idx)| {
        quote! { (#name, #idx), }
    });

    quote! {
        pub mod aliases {
            use super::*;

            pub const ALIASES: &[(&str, usize)] = &[
                #(#entries)*
            ];

            #[doc = "resolve an alias name to its Peripheral"]
            pub fn resolve(alias: &str) -> Option<&'static Peripheral> {
                ALIASES
                    .iter()
                    .find(|(k, _)| *k == alias)
                    .and_then(|(_, idx)| peripheral_by_node(*idx))
            }
        }
    }
}

