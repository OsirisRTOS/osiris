use crate::ir::{DeviceTree, PropValue};
use proc_macro2::TokenStream;
use quote::quote;

pub fn generate_rust(dt: &DeviceTree) -> String {
    let segments: &[TokenStream] = &[
        emit_prop_value_type(),
        emit_topology_type(),
        emit_peripheral_type(),
        emit_nodes(dt),
        emit_peripherals(dt),
        emit_query_api(),
        emit_aliases_module(dt),
        emit_memory_module(dt),
        emit_chosen_module(dt),
    ];

    let combined = segments.iter().cloned().collect::<TokenStream>();
    let wrapped = quote! {
        pub mod device_tree {
            #combined
        }
    };

    let file: syn::File = syn::parse2(wrapped).unwrap();
    let mut out = emit_header();
    out.push_str(&prettyplease::unparse(&file));
    out
}

// ------------------------------------------------------------------------------------------------
// File header
// ------------------------------------------------------------------------------------------------

fn emit_header() -> String {
    r#"// ------------------------------------------------------------------------------------------------
// AUTOGENERATED BY dtgen - DO NOT EDIT FILE
// ------------------------------------------------------------------------------------------------

"#
    .to_string()
}

// ------------------------------------------------------------------------------------------------
// Type defitions
// ------------------------------------------------------------------------------------------------

fn emit_prop_value_type() -> TokenStream {
    quote! {
        #[allow(dead_code)]
        #[derive(Debug, Clone, Copy, PartialEq)]
        pub enum PropValue {
            Empty,
            U32(u32),
            U32Array(&'static [u32]),
            Str(&'static str),
            StringList(&'static [&'static str]),
            Bytes(&'static [u8]),
        }
    }
}

fn emit_topology_type() -> TokenStream {
    quote! {
        #[allow(dead_code)]
        #[derive(Debug, Clone, Copy)]
        pub struct TreeNode {
            pub name:     &'static str,
            pub phandle:  Option<u32>,
            pub parent:   Option<usize>,
            pub children: &'static [usize],
        }

        impl TreeNode {
            #[doc = "returns the parent TreeNode, if any"]
            pub fn parent_node(&self) -> Option<&'static TreeNode> {
                self.parent.map(|idx| &NODES[idx])
            }

            #[doc = "iterate children as (node_index, &TreeNode)"]
            pub fn iter_children(&self) -> impl Iterator<Item = (usize, &'static TreeNode)> {
                self.children.iter().map(|&idx| (idx, &NODES[idx]))
            }
        }
    }
}

fn emit_peripheral_type() -> TokenStream {
    quote! {
        #[allow(dead_code)]
        #[derive(Debug, Clone, Copy)]
        pub struct Peripheral {
            pub node:       usize,
            pub compatible: &'static [&'static str],
            pub reg:        Option<(usize, usize)>,
            pub interrupts: &'static [u32],
            pub phandle:    Option<u32>,
            pub props:      &'static [(&'static str, PropValue)],
        }

        impl Peripheral {
            #[doc = "returns true if any compatible string exactly matches `c`"]
            pub fn is_compatible(&self, c: &str) -> bool {
                self.compatible.iter().any(|&s| s == c)
            }

            #[doc = "returns true if any compatible string contains `fragment` as a substring"]
            pub fn compatible_contains(&self, fragment: &str) -> bool {
                self.compatible.iter().any(|&s| s.contains(fragment))
            }

            #[doc = "look up a prop by key, returning the raw PropValue"]
            pub fn prop(&self, key: &str) -> Option<PropValue> {
                self.props.iter().find(|(k, _)| *k == key).map(|(_, v)| *v)
            }

            #[doc = "get a u32 prop"]
            pub fn prop_u32(&self, key: &str) -> Option<u32> {
                match self.prop(key) {
                    Some(PropValue::U32(v)) => Some(v),
                    _ => None,
                }
            }

            #[doc = "get a str prop"]
            pub fn prop_str(&self, key: &str) -> Option<&'static str> {
                match self.prop(key) {
                    Some(PropValue::Str(s)) => Some(s),
                    _ => None,
                }
            }

            #[doc = "get a u32 array prop"]
            pub fn prop_u32_array(&self, key: &str) -> Option<&'static [u32]> {
                match self.prop(key) {
                    Some(PropValue::U32Array(arr)) => Some(arr),
                    _ => None,
                }
            }

            #[doc = "returns the base address from reg, if present"]
            pub fn reg_base(&self) -> Option<usize> {
                self.reg.map(|(base, _)| base)
            }

            #[doc = "returns the size from reg, if present"]
            pub fn reg_size(&self) -> Option<usize> {
                self.reg.map(|(_, size)| size)
            }

            #[doc = "resolve a phandle value (e.g. from a clocks prop) to another Peripheral"]
            pub fn resolve_phandle(&self, ph: u32) -> Option<&'static Peripheral> {
                peripheral_by_phandle(ph)
            }

            #[doc = "returns the TreeNode for this peripheral"]
            pub fn tree_node(&self) -> &'static TreeNode {
                &NODES[self.node]
            }

            #[doc = "returns true if status prop is absent or set to \"okay\""]
            pub fn is_enabled(&self) -> bool {
                match self.prop_str("status") {
                    Some(s) => s == "okay",
                    None => true,
                }
            }
        }
    }
}

